/*----------------- Operators -----------------*/
function Addition(a, b) {
    return Number(a) + Number(b);
}

function Subtraction(a, b) {
    return Number(a) - Number(b);
}

function Multiply(a, b) {
    return a * b
}

function Division(a, b) {
    return a / b
}

