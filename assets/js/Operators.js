function sin(a) {
    return Math.sin(a);
}

function cos(a) {
    return Math.cos(a);
}

function tan(a) {
    return Math.tan(a);
}

function log(a) {
    return Math.log10(a);
}


function power(a,b) {
    return Math.pow(a,b);
}